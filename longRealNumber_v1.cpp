#include <iostream>
#include <string>

int main() {
    std::string input; // Переменная для хранения пользовательского ввода
    std::cout << "Enter a string: "; // Запрашиваем ввод у пользователя
    std::getline(std::cin, input); // Считываем строку

    bool hasDigits = false; // Флаг для проверки наличия хотя бы одной цифры
    bool hasDecimalPoint = false; // Флаг для проверки наличия десятичной точки
    bool isValid = true; // Флаг для проверки валидности строки

    // Проверяем, что строка не пустая и обрабатываем первый символ
    if (input[0] == '-') {
        // Если первый символ минус, продолжаем проверку
    } else if (input[0] == '.') {
        hasDecimalPoint = true; // Если первый символ точка, устанавливаем флаг
    } else if (input[0] < '0' || input[0] > '9') {
        isValid = false; // Если первый символ не цифра, точка или минус, строка некорректна
    }

    // Проходим по всем символам строки начиная со второго символа
    for (size_t i = 1; i < input.length(); i++) {
        char currentChar = input[i];

        // Проверяем, является ли символ цифрой
        if (currentChar >= '0' && currentChar <= '9') {
            hasDigits = true; // Если нашли цифру, устанавливаем флаг
        }
        // Проверяем наличие десятичной точки
        else if (currentChar == '.') {
            if (hasDecimalPoint) {
                isValid = false; // Если точка уже была, строка некорректна
                break;
            }
            hasDecimalPoint = true; // Устанавливаем флаг, что точка найдена
        }
        // Проверяем на недопустимые символы (допустим только '-' в начале)
        else if (currentChar != '-') {
            isValid = false; // Если символ не цифра и не точка, строка некорректна
            break; // Выходим из цикла, если нашли ошибку
        }

        // Дополнительная проверка: если '-' не в начале, то это ошибка
        if (currentChar == '-' && i > 0) {
            isValid = false;
            break;
        }
    }

    // Проверяем, что была хотя бы одна цифра и строка валидна
    if (isValid && hasDigits) {
        std::cout << "Yes\n"; // Если всё корректно, выводим Yes
    } else {
        std::cout << "No\n"; // В противном случае выводим No
    }

    return 0; // Завершаем программу
}
